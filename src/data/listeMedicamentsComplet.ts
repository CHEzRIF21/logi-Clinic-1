/**
 * Fonction pour nettoyer et normaliser le nom d'un médicament
 */
export function normaliserNomMedicament(nom: string): string {
  return nom
    .trim()
    .replace(/\s+/g, ' ') // Remplacer les espaces multiples par un seul
    .toUpperCase();
}

/**
 * Fonction pour extraire la forme pharmaceutique depuis le nom
 */
export function extraireForme(nom: string): string {
  const formes = [
    'CP', 'COMPRIME', 'COMPRIMES', 'BLISTER',
    'GELULE', 'GELULES', 'CAPS', 'CAPSULES',
    'SOL INJ', 'SOLUTION INJECTABLE', 'INJ', 'INJECTABLE', 'AMP', 'AMPOULES',
    'SUSP', 'SUSPENSION', 'SIROP', 'SOL BUV',
    'CREME', 'POMMADE', 'GEL',
    'SUPPO', 'SUPPOSITOIRE',
    'AEROSOL', 'SPRAY',
    'PDRE', 'POUDRE',
    'SOL OPHT', 'SOLUTION OPHTALMIQUE',
    'SOL P PERF', 'SOLUTION POUR PERFUSION',
    'SOL P INHAL', 'SOLUTION POUR INHALATION',
  ];
  
  const nomUpper = nom.toUpperCase();
  for (const forme of formes) {
    if (nomUpper.includes(forme)) {
      if (forme === 'CP' || forme === 'COMPRIME' || forme === 'COMPRIMES') return 'Comprimé';
      if (forme === 'GELULE' || forme === 'GELULES' || forme === 'CAPS' || forme === 'CAPSULES') return 'Gélule';
      if (forme.includes('INJ') || forme.includes('INJECTABLE') || forme === 'AMP' || forme === 'AMPOULES') return 'Injection';
      if (forme.includes('SUSP') || forme === 'SIROP' || forme.includes('BUV')) return 'Sirop';
      if (forme === 'CREME' || forme === 'POMMADE' || forme === 'GEL') return 'Pommade';
      if (forme.includes('SUPPO')) return 'Suppositoire';
      if (forme.includes('AEROSOL') || forme === 'SPRAY') return 'Aérosol';
      if (forme.includes('PDRE') || forme.includes('POUDRE')) return 'Poudre';
      if (forme.includes('OPHT')) return 'Solution ophtalmique';
      if (forme.includes('PERF')) return 'Solution pour perfusion';
      if (forme.includes('INHAL')) return 'Solution pour inhalation';
    }
  }
  
  return 'Autres';
}

/**
 * Fonction pour extraire le dosage depuis le nom
 */
export function extraireDosage(nom: string): string {
  // Recherche de patterns de dosage comme "500MG", "1G", "10ML", etc.
  const dosageMatch = nom.match(/(\d+(?:[.,]\d+)?)\s*(MG|G|ML|UI|µG|%|MG\/ML|UI\/ML)/i);
  if (dosageMatch) {
    return dosageMatch[0].toUpperCase();
  }
  return '';
}

/**
 * Fonction pour déterminer l'unité depuis le nom
 */
export function extraireUnite(nom: string): string {
  if (nom.toUpperCase().includes('CP') || nom.toUpperCase().includes('COMPRIME')) return 'Boîte';
  if (nom.toUpperCase().includes('AMP') || nom.toUpperCase().includes('AMPOULES')) return 'Ampoule';
  if (nom.toUpperCase().includes('FL') || nom.toUpperCase().includes('FLACON')) return 'Flacon';
  if (nom.toUpperCase().includes('TUBE')) return 'Tube';
  if (nom.toUpperCase().includes('SACHET')) return 'Sachet';
  if (nom.toUpperCase().includes('BTE') || nom.toUpperCase().includes('BOITE')) return 'Boîte';
  return 'Unité';
}
